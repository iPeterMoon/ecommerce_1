public class Resenia {

    Long id;
    Producto producto;
    String comentario;
    LocalDate fechaRealizada;
    int calificacion;
    Usuario cliente;

    public Resenia() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Producto getProducto() {
        return producto;
    }

    public void setProducto(Producto producto) {
        this.producto = producto;
    }

    public String getComentario() {
        return comentario;
    }

    public void setComentario(String comentario) {
        this.comentario = comentario;
    }

    public LocalDate getFechaRealizada() {
        return fechaRealizada;
    }

    public void setFechaRealizada(LocalDate fechaRealizada) {
        this.fechaRealizada = fechaRealizada;
    }

    public int getCalificacion() {
        return calificacion;
    }

    public void setCalificacion(int calificacion) {
        this.calificacion = calificacion;
    }

    public Usuario getCliente() {
        return cliente;
    }

    public void setCliente(Usuario cliente) {
        this.cliente = cliente;
    }

    @Override
    public String toString() {
        return "Resenia{" + "id=" + id + ", producto=" + producto + ", comentario=" + comentario + ", fechaRealizada=" + fechaRealizada + ", calificacion=" + calificacion + ", cliente=" + cliente + '}';
    }

}
